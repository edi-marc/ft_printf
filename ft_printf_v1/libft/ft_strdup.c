#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*str;

	len = ft_strlen(s1) + 1;
	str = ft_calloc(len, sizeof(*str));
	if (str)
		ft_memcpy(str, s1, len);
	return (str);
}
